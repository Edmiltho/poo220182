package Classes;

public class Veiculo {
    String placa;
    String modelo;
    String marca;
    String cor;
    
    public Veiculo(){}
    
    public Veiculo(String placa, String modelo, String marca, String cor) {
        this.placa = placa;
        this.modelo = modelo;
        this.marca = marca;
        this.cor = cor;
    }
}